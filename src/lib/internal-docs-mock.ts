/**
 * 質問に対する事前用意の応答を取得
 * @returns マッチした Q&A、または null（自由記述で事前応答なし）
 */
export function getInternalDocAnswer(query: string): InternalDocAnswer | null {
  const normalized = query.replace(/\s+/g, '').toLowerCase();
  // サンプル質問のいずれかに完全一致 or 部分一致
  for (const qa of SAMPLE_QA) {
    if (qa.question === query) return qa;
  }
  // 部分一致（キーワードマッチ）
  for (const qa of SAMPLE_QA) {
    const qNorm = qa.question.replace(/\s+/g, '').toLowerCase();
    if (normalized.includes(qNorm.slice(0, 5)) || qNorm.includes(normalized.slice(0, 5))) {
      return qa;
    }
  }
  return null;
}
