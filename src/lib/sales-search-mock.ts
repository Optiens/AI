/**
 * 質問に対する事前用意の応答を取得
 */
export function getSalesAnswer(query: string): SalesAnswer | null {
  const normalized = query.replace(/\s+/g, '').toLowerCase();
  for (const qa of SAMPLE_SALES_QA) {
    if (qa.question === query) return qa;
  }
  for (const qa of SAMPLE_SALES_QA) {
    const qNorm = qa.question.replace(/\s+/g, '').toLowerCase();
    if (normalized.includes(qNorm.slice(0, 6)) || qNorm.includes(normalized.slice(0, 6))) {
      return qa;
    }
  }
  return null;
}
